class Mathematics1 {
	long add(int[] arr) {
		long result = arr[0] + arr[1];
			return result;
			}
	
		long divide(int[] arr) {
			long result = arr[0] / arr[1];
			return result;
			}
			
			long multiply(int[] arr) {
				long result = arr[0] * arr[1];
			return result;
				}
			
			long substract(int[] arr) {
				long result = arr[0] - arr[1];
			return result;
				}
				}
	