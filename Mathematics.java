class Mathematics {
int num1 = 50;
int num2 = 20;

	Mathematics() {
	System.out.println();
	}
	
	long add() {
		return (num1 + num2);
	}
	
	long divide() {
		return (num1 / num2);
	}
	
	long multiply() {
		return (num1 * num2);
	}
	
	long subtract() {
		return (num1 - num2);
	}
}
	