class ArithmeticOperations {
	void add(int num1, int num2) {
		int result = num1 + num2;
		
		System.out.println(result);
	}
	
	void divide(int num1, int num2) {
		int result = num1 / num2;
		
		System.out.println(result);
	}
	
	void subtract(int num1, int num2) {
		int result = num1 - num2;
		
		System.out.println(result);
	}
	
	void multiply(int num1, int num2) {
		int result = num1 * num2;
		
		System.out.println(result);
	}
}
	