class Maths {
	int data1;
	int data2;
	
	Maths(int value1, int value2) {
	data1 = value1;
	data2 = value2;
	}
	
	long add() {
	return data1 + data2;
	}
	
	long divide() {
	return data1 / data2;
	}
	
	long multiply() {
	return data1 * data2;
	}
	
	long subtract() {
	return data1 - data2;
	}
	}
	
	